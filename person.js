class person {
        constructor(Name,Age,PhoneNumber,EmailId,Gender){
            this.Name=Name;
            this.Age=Age;
            this.PhoneNumber=PhoneNumber;
            this.EmailId=EmailId;
            this.Gender=Gender;
        }
    }
